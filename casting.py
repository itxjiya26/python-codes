# Example to show how casting works in Python

# Integer to float
num1 = 10
float_num = float(num1)
print("Integer to float:", float_num)

# Float to integer
num2 = 12.75
int_num = int(num2)
print("Float to integer:", int_num)

# String to integer
num3 = "50"
int_from_str = int(num3)
print("String to integer:", int_from_str)

# Integer to string
num4 = 100
str_num = str(num4)
print("Integer to string:", str_num)

# String to float
num5 = "45.6"
float_from_str = float(num5)
print("String to float:", float_from_str)

